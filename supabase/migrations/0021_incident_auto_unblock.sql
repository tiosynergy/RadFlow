--  Запускати у Supabase → SQL Editor ПІСЛЯ 0020_no_booking_during_incident.sql.
--
--  blocked_until — ЗАВЖДИ жорстка межа блокування (кабінет блокується лише у вікні
--  [started_at, blocked_until)). Прапорець auto_unblock керує лише долею запису
--  ПІСЛЯ завершення вікна:
--    • auto_unblock = true  (типово) — інцидент знімається автоматично,
--      кабінет і черга відновлюються самі;
--    • auto_unblock = false — кабінет так само звільняється наприкінці вікна,
--      але запис лишається й чекає на ручне підтвердження зняття («🔓 Розблокувати»).
--  Старі рядки отримують true (поведінка за замовчуванням).
-- ============================================================

alter table public.incidents
  add column if not exists auto_unblock boolean not null default true;
